public class day37 {
	public static void main(String[] args) {
		System.out.println("operator penugasan:penugasan nilai , pertambahan , dan perkurangan");
		byte angka ;
		// pengisian nilai(=)
		angka = 50;
		System.out.println("nilai angka : " + angka) ;
		//penugasan dan penambahan (+=)
		angka += 30;
		System.out.println("nilai angka setelah ditambah : " + angka) ;
		//penugasan dan pengurangan(-=)
		angka -= 20;
		System.out.println("nilai angka setelah dikurang : " + angka) ;
	}
}
