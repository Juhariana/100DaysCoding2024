public class day38 {
	public static void main(String[] args) {
		
		System.out.println("operator penugasan:pengisian dan perkalian,pengisian dan pembagian");
		
		byte angka = 11;
		System.out.println("nilai angka : " + angka);
	
			//penugasan dan perkalian (*=)
		angka *= 5;
		System.out.println("nilai angka setelah dikali : " + angka) ;
		//penugasan dan pembagian(%=)
		angka %= 2;
		System.out.println("sisa bagi variabel angka setelah dibagi: " + angka) ;
	}
      }
